

public class Student {
    private String studentId;
    private String name;
    private int korGr;
    private int engGr;
    private int matGr;

    public Student(String studentId, String name, int korGr, int engGr, int matGr) {
        this.studentId = studentId;
        this.name = name;
        this.korGr = korGr;
        this.engGr = engGr;
        this.matGr = matGr;
    }

}

